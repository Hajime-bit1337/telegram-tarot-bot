#–ï–∂–µ–¥–Ω–µ–≤–Ω–∞—è —Ä–∞—Å—Å—ã–ª–∫–∞
async def send_daily_tarot(app):
    for chat_id, role in user_roles.items():
        text = generate_tarot(for_matvey=(role == "matvey"))
        await app.bot.send_message(chat_id=chat_id, text=text)


#–ù–∞—Å—Ç—Ä–æ–π–∫–∞ –ø–ª–∞–Ω–∏—Ä–æ–≤—â–∏–∫–∞
def setup_scheduler(app):
    scheduler = BackgroundScheduler(timezone="Europe/Moscow")
    scheduler.add_job(lambda: app.create_task(send_daily_tarot(app)), 'cron', hour=2, minute=20)
    scheduler.start()


